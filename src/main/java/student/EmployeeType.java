package student;

/**
 * Represents the different types of employees in the payroll system.
 * This enum is used to distinguish between hourly and salaried employees,
 * which have different pay calculation methods.
 */
public enum EmployeeType {
    /** Represents an employee paid on an hourly basis. */
    HOURLY,
    /** Represents an employee paid on a salary basis. */
    SALARY
}
